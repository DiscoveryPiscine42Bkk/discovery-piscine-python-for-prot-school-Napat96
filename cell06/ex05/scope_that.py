#!/usr/bin/env python3

# Define the add_one method
def add_one(num):
    return num + 1

# Initialize a variable
my_var = 10

# Display the variable before calling the method
print("Before calling add_one:", my_var)

# Call the add_one method and update the variable
my_var = add_one(my_var)

# Display the variable after calling the method
print("After calling add_one:", my_var)
