# เริ่มต้นลูปสำหรับตารางคูณ 0 ถึง 10
i = 0
while i <= 10:
    print(f"Table de {i}:", end=" ")
    
    # ลูปสำหรับการคูณจาก 0 ถึง 10
    j = 0
    while j <= 10:
        # แสดงผลคูณระหว่าง i และ j โดยไม่ขึ้นบรรทัดใหม่
        print(i * j, end=" ")
        j += 1
    
    # ขึ้นบรรทัดใหม่หลังจากแสดงตารางคูณของ i
    print()
    i += 1
